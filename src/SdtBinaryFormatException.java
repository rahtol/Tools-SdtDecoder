
public class SdtBinaryFormatException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 3436434197145923311L;

	public SdtBinaryFormatException() {
	}

	public SdtBinaryFormatException(String arg0) {
		super(arg0);
	}

	public SdtBinaryFormatException(Throwable cause) {
		super(cause);
	}

	public SdtBinaryFormatException(String message, Throwable cause) {
		super(message, cause);
	}

	public SdtBinaryFormatException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
